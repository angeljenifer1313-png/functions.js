let numbers = [10, 20, 30];

console.log(numbers); 

// push
numbers.push(40);
console.log(numbers); 

// pop
numbers.pop();
console.log(numbers); 


// unshift
numbers.unshift(5);
console.log(numbers); 


// shift
numbers.shift();
console.log(numbers); 


//loop
let numbers = [10, 20, 30, 40, 50];

for (let i = 0; i < numbers.length; i++) {
  console.log(numbers[i]);
}